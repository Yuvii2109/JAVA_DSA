public class Regulations {
    public static void main(String[] args) {
        System.out.println();
        System.out.println(5&6);
        System.out.println(5|6);
        System.out.println(5^6);
        System.out.println(~5);

        // Special case - 
        System.out.println(~0);

        System.out.println(5<<2);
        System.out.println(6>>1);
        System.out.println();
    }
}