public class Datatypes {
    public static void main(String args[]){
        // Primitive Data Types
        byte a = 10; // 8-bit signed integer
        System.out.println(a);
        char ch = 'Y'; // "" -> for string '' -> for char
        System.out.println(ch);
        boolean var = true; // Sirf true false ke liye uplabdh
        System.out.println(var);
        float num = 21.23f; // Yeh store toh decimal main nahi ho rha but print usi main krega
        System.out.println(num);
        int x = 7;
        System.out.println(x);
        double y = 23.21; // Yeh store bhi decimal main hua hai print bhi decimal main krega
        System.out.println(y);
        // Long is used to store bde bde non decimal numbers
        long l = 1000000000L;
        // L is used to specify that the number is a long integer
        System.out.println(l);
        // Short chote numbers ke liye use hota hai bhayyy
        short s = 1000;
        System.out.println(s);
    }
}